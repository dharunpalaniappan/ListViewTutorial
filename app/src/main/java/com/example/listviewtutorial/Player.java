package com.example.listviewtutorial;

public class Player {
    String playername;
    String tag;

    public Player(String playername, String tag) {
        this.playername = playername;
        this.tag = tag;
    }
        public String getplayer()
        {
            return playername;
        }

        public String gettag()
        {
            return tag;
        }
}
